// One chunk per Q&A so retrieval scores each question independently.
export function getFaqChunks() {
  return FAQ.flatMap(({ category, items }) =>
    items.map(({ q, a }) => ({ title: `FAQ — ${category}`, text: `Q: ${q}\nA: ${a}` }))
  );
}
